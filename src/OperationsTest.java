public class OperationsTest {

	public static void main(String[] args) {
		
		int kb; 
		kb = 6; 
		System.out.println(kb);
		kb = kb + 5; 
		System.out.println(kb + ", Added 5");
		kb = kb /2; 
		System.out.println(kb + ", Divided by 2");
		kb = kb *4; 
		System.out.println(kb + ", Multiplied by 4");
		kb = kb -6; 
		System.out.println(kb + ", Subtracted 6");
		kb = kb %2; 
		System.out.println(kb + ", Divided Modularly by 2");
		
		
	}

}
